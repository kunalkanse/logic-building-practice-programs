int main()
{
    PNODE first = NULL;
    int iRet = 0;

    insertFirst(&first , 51);   // insertFirst(50,51);
    insertFirst(&first , 21);
    insertFirst(&first , 11);

    insertLast(&first , 101);

    Display(first);

    iRet = Count(first);
    printf("\n\nCount of the elements : %d\n\n",iRet);

    printf("DeleteFirst : ");
    deleteFirst(&first);
    Display(first);

    iRet = Count(first);
    printf("\n\nCount of the elements : %d\n\n",iRet);

    printf("DeleteLast : ");
    deleteLast(&first);
    Display(first);

    iRet = Count(first);
    printf("\n\nCount of the elements : %d\n\n",iRet);

    return 0;
}

