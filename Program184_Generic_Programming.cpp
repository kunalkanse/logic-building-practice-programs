/////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////
//Generic function
// this function can work for any datatype
/////////////////////////////////////////////////////////////////////

template <class T>
void SwapX(T *p , T *q)
{
    T temp;

    temp = *p;
    *p = *q;
    *q = temp;
}

////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////
// main function
//////////////////////////////////////////////////////////////////////////

int main()
{
    char a = '\0', b = '\0';
    int c = 0, d = 0;
    float e = 0.0, f = 0.0;

    //////////////////////////////////////////////////////////////
    // passing character data
    //////////////////////////////////////////////////////////////

    cout<<"Enter the first character : ";
    cin>>a;

    cout<<"Enter the second character : ";
    cin>>b;

    SwapX(&a,&b);

    cout<<"Value of a becomes : "<<a<<"\n";
    cout<<"Value of b becomes : "<<b<<"\n";

    /////////////////////////////////////////////////////////////
    // passing integer values
    /////////////////////////////////////////////////////////////

    cout<<"\nEnter the first int value : ";
    cin>>c;

    cout<<"Enter the second int value : ";
    cin>>d;

    SwapX(&c,&d);

    cout<<"Value of c becomes : "<<c<<"\n";
    cout<<"Value of d becomes : "<<d<<"\n";

    ///////////////////////////////////////////////////////////////
    // passing float values
    /////////////////////////////////////////////////////////////////

    cout<<"\nEnter the first float value : ";
    cin>>e;

    cout<<"Enter the second float value : ";
    cin>>f;

    SwapX(&e,&f);

    cout<<"Value of e becomes : "<<e<<"\n";
    cout<<"Value of f becomes : "<<f<<"\n";

    ///////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////

    return 0;
}
